# %占位符
name = 'yangyijn'
age = 28
print('我的名字是 %s,年龄是 %d' % (name, age))
print('我的名字是 {0}，我的年龄是 {1}'.format(name, age))

# f{}占位符
print(f'我的名字是 {name},我的年龄是 {age}')
